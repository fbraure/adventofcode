const binToInt = (bin) => {
  return parseInt(bin, 2);
}
const intToBin = (int) => {
  return int.toString(2).padStart(36, "0");
}
const applyMask = (int, mask) => {
  // console.log(int)
  let bin = intToBin(int)
  // console.log(bin)
  bin = bin.split('')
  // console.log(mask)
  let maskSplitted = mask.split('')
  for(let i = 0; i < maskSplitted.length; i++){
    if(maskSplitted[i] !== "X"){
      bin[i] = maskSplitted[i]
    }
  }
  bin = bin.join("")
  // console.log(bin)
  let newInt = binToInt(bin)
  // console.log(newInt)
  return newInt
}

