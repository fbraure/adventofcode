const getNbTrees = (right, down) =>{
  let nbTrees = 0;
  let iRow = 0;
  let iCol = 0;
  while(true) {
    iCol += right
    iRow += down
    if(iRow >= inputs.length){
      break;
    }
    if (iCol >=  inputs[iRow].length){
      iCol = iCol%(inputs[iRow].length)
    }
    if(iRow >= inputs.length){
      break;
    }
    if(inputs[iRow][iCol] === "#") {++nbTrees}
  }
  // console.log(`right: ${right} down: ${down}  Tress: ${nbTrees}`)
  return nbTrees
}
